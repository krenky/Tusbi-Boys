        //}
        // POST: api/Products
        // To protect from overposting attacks, see https://go.microsoft.com/fwlink/?linkid=2123754
        [HttpPost]
        public async Task<ActionResult<Product>> PostProduct(string name, string description, string title, decimal price, List<string> photos, string userId)
        {
            Product product = new Product{ Name = name, Description = description, Title = title, Price = price, Photos = photos, UserId = userId};
            _context.Products.Add(product);
            await _context.SaveChangesAsync();

            return CreatedAtAction("GetProduct", new { id = product.Id }, product);
        }

        // DELETE: api/Products/5
        [HttpDelete("{id}")]
        public async Task<IActionResult> DeleteProduct(int id)
        {
            var product = await _context.Products.FindAsync(id);
            if (product == null)
            {
                return NotFound();
            }

            _context.Products.Remove(product);
            await _context.SaveChangesAsync();

            return NoContent();
        }
        //controller with paginate
        [HttpGet("/Products")]
        public async Task<IEnumerable<Product>> GetProductsAsync([FromQuery] PaginateParameters paginateParameters)
        {
            return _context.Products.ToList()
                .Skip((paginateParameters.PageNumber - 1) * paginateParameters.PageSize)
                .Take(paginateParameters.PageSize)
                .ToList();
        }

        private bool ProductExists(int id)
        {
            return _context.Products.Any(e => e.Id == id);
        }

    }
}
